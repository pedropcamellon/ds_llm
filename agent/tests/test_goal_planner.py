def test_full_realistic_nearby_list(planner):
    """Full realistic nearby list from live log — correct set of actions generated."""
    nearby = [
        {"type": "other", "name": "snow", "distance": 0},
        {"type": "other", "name": "rain", "distance": 0},
        {"type": "other", "name": "flower", "distance": 5.4},
        {"type": "other", "name": "seeds", "distance": 7.2},
        {"type": "other", "name": "flower", "distance": 7.3},
        {"type": "other", "name": "flies", "distance": 8.5},
        {"type": "harvestable", "name": "pighead", "distance": 8.5},
        {"type": "other", "name": "robin", "distance": 8.7},
        {"type": "harvestable", "name": "sapling", "distance": 11.5},
        {"type": "harvestable", "name": "evergreen", "distance": 11.7},
        {"type": "harvestable", "name": "berrybush", "distance": 16.8},
        {"type": "harvestable", "name": "grass", "distance": 17.6},
        {"type": "other", "name": "flint", "distance": 24.9},
        {"type": "harvestable", "name": "rabbithole", "distance": 26.2},
    ]
    state = dict(_EMPTY_STATE, nearby_entities=nearby)
    inv_with_axe = {"axe": 1}
    actions = planner.get_concrete_actions(inv_with_axe, state)
    flat = " ".join(actions)

    # ground items recognised despite type="other"
    assert any("pick_up_item:seeds" in a for a in actions)
    assert any("pick_up_item:flint" in a for a in actions)

    # harvestables correctly mapped (axe present for evergreen)
    assert any("gather_resource:twigs" in a for a in actions)  # sapling
    assert any("gather_resource:log" in a for a in actions)  # evergreen (axe required)
    assert any("gather_resource:berries" in a for a in actions)  # berrybush
    assert any("gather_resource:cutgrass" in a for a in actions)  # grass

    # ambient noise absent
    assert "pick_up_item:snow" not in flat
    assert "pick_up_item:rain" not in flat
    assert "pick_up_item:flower" not in flat
    assert "pick_up_item:flies" not in flat
    assert "pick_up_item:robin" not in flat

    # always-present fallbacks
    assert any("explore" == a for a in actions)
    assert any("idle" == a for a in actions)
