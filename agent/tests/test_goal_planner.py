# ── Real game-state nearby patterns (from live conversation log) ──────────────
#
# The Lua exporter uses:
#   type="other"        for loose ground items (seeds, flint) AND noise (snow, rain, flowers)
#   type="harvestable"  for saplings, grass, evergreens, berrybushes, etc.
#
# The builder must distinguish pickable items from ambient noise purely by name.

# Typical early-game nearby list: mostly noise, one "seeds" on the ground
_NEARBY_NOISE_PLUS_SEEDS = [
    {"type": "other", "name": "snow", "distance": 0},
    {"type": "other", "name": "rain", "distance": 0},
    {"type": "other", "name": "flower", "distance": 5.4},
    {"type": "other", "name": "seeds", "distance": 7.2},
    {"type": "other", "name": "flower", "distance": 7.3},
]


def test_seeds_type_other_produces_pickup(planner):
    """seeds with type='other' must appear as pick_up_item despite non-item type."""
    state = dict(_EMPTY_STATE, nearby_entities=_NEARBY_NOISE_PLUS_SEEDS)
    actions = planner.get_concrete_actions({}, state)
    assert any("pick_up_item:seeds" in a for a in actions)


def test_ambient_noise_does_not_produce_pickup(planner):
    """snow, rain, flower, robin etc. must NOT produce any action."""
    state = dict(_EMPTY_STATE, nearby_entities=_NEARBY_NOISE_PLUS_SEEDS)
    actions = planner.get_concrete_actions({}, state)
    flat = " ".join(actions)
    assert "pick_up_item:snow" not in flat
    assert "pick_up_item:rain" not in flat
    assert "pick_up_item:flower" not in flat


