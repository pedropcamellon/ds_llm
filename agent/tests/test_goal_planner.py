"""Tests for ActionPlanner — prerequisite-based action filtering + concrete action generation."""

import pytest
from action_planner import ActionPlanner as GoalPlanner


@pytest.fixture
def planner():
    return GoalPlanner()


_EMPTY_STATE: dict = {
    "health": 80,
    "hunger": 80,
    "sanity": 150,
    "nearby_entities": [],
    "threats": [],
}


# ── Empty inventory (base get_valid_actions) ──────────────────────────────────


def test_empty_inv_always_valid_actions(planner):
    valid = planner.get_valid_actions({})
    assert "explore" in valid
    assert "idle" in valid
    assert "eat_food" in valid
    assert "run_from_enemy" in valid
    # gather_resource is no longer a base ACTION_SPEC — it's generated by get_concrete_actions
    assert "gather_resource" not in valid


def test_empty_inv_tool_actions_blocked(planner):
    valid = planner.get_valid_actions({})
    assert "chop_tree" not in valid
    assert "mine_rock" not in valid


def test_empty_inv_craft_actions_blocked(planner):
    valid = planner.get_valid_actions({})
    assert "craft_item:axe" not in valid
    assert "craft_item:pickaxe" not in valid


# ── Tool prerequisites ────────────────────────────────────────────────────────


def test_axe_unlocks_chop_tree(planner):
    assert "chop_tree" in planner.get_valid_actions({"axe": 1})


def test_pickaxe_unlocks_mine_rock(planner):
    assert "mine_rock" in planner.get_valid_actions({"pickaxe": 1})


def test_partial_craft_prereq_still_blocked(planner):
    # Need twig + flint; only have twig
    valid = planner.get_valid_actions({"twigs": 1})
    assert "craft_item:axe" not in valid


def test_exact_craft_prereqs_unlock_craft(planner):
    assert "craft_item:axe" in planner.get_valid_actions({"twigs": 1, "flint": 1})


def test_surplus_prereqs_still_valid(planner):
    assert "craft_item:axe" in planner.get_valid_actions({"twigs": 5, "flint": 3})


def test_craft_axe_suppressed_when_already_held(planner):
    """craft_item:axe must NOT appear when axe is already in inventory."""
    valid = planner.get_valid_actions({"twigs": 1, "flint": 1, "axe": 1})
    assert "craft_item:axe" not in valid


def test_craft_pickaxe_suppressed_when_already_held(planner):
    """craft_item:pickaxe must NOT appear when pickaxe is already in inventory."""
    valid = planner.get_valid_actions({"twigs": 2, "flint": 2, "pickaxe": 1})
    assert "craft_item:pickaxe" not in valid


def test_craft_torch_not_suppressed_when_already_held(planner):
    """Consumables like torch are NOT redundant_if_held — can craft more even if one exists."""
    valid = planner.get_valid_actions({"twigs": 2, "cutgrass": 2, "torch": 1})
    assert "craft_item:torch" in valid


# ── Alias normalisation ───────────────────────────────────────────────────────


def test_twig_alias_normalised_to_twigs(planner):
    # "twig" (singular) should be treated as "twigs"
    assert "craft_item:axe" in planner.get_valid_actions({"twig": 1, "flint": 1})


def test_rock_alias_normalised(planner):
    assert "mine_rock" in planner.get_valid_actions({"pickaxe": 1})


# ── Format helpers ────────────────────────────────────────────────────────────


def test_format_valid_actions_is_sorted_csv(planner):
    result = planner.format_valid_actions({})
    # Extract bare action names (strip cost annotations like "craft_item:axe (...)")
    parts = [p.strip().split(" (")[0] for p in result.split(",")]
    assert parts == sorted(parts)


# ── get_concrete_actions ──────────────────────────────────────────────────────


def test_concrete_always_includes_explore_and_idle(planner):
    actions = planner.get_concrete_actions({}, _EMPTY_STATE)
    flat = " ".join(actions)
    assert "explore" in flat
    assert "idle" in flat


def test_concrete_eat_food_lists_specific_item(planner):
    inv = {"berries": 3, "twigs": 2}
    actions = planner.get_concrete_actions(inv, _EMPTY_STATE)
    assert any("eat_food:berries" in a for a in actions)


